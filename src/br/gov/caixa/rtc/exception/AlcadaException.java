package br.gov.caixa.rtc.exception;

public class AlcadaException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AlcadaException() {
		super();

	}

	public AlcadaException(String message, Throwable cause) {
		super(message, cause);

	}

	public AlcadaException(String message) {
		super(message);

	}

	public AlcadaException(Throwable cause) {
		super(cause);

	}

}
